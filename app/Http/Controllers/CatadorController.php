     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        Catador::create([
            'nome' => mb_strtoupper($request->nome, 'UTF-8'),
            'cep' => $request->cep,
            'telefone' => $request->telefone,
            'status' => $request->status,
        ]);

        return redirect()->route('catadors.index');
    }

